package week1.data_types;

public class DataTypes {
    public static void main(String[] args) {
        char initials = 'E';
        System.out.println("My initials: " + initials);

        int populationGermany = 83240000;
        System.out.println("Population in Germany: " + populationGermany);

        long populationEarth = 7800000000L;
        System.out.println("Population on Earth: " + populationEarth);

        boolean isDaytime = true;
        System.out.println("Is it currently daytime? " + isDaytime);

        float goalsPerGame = 0.5f;
        System.out.println("Mario Gomez's goals per game at Bayern MÃ¼nchen: " + goalsPerGame);

        short lengthInWeeks = 12;
        System.out.println("Length of the Java program in weeks: " + lengthInWeeks);

        double pi = Math.PI;
        System.out.println("The mathematical number PI: " + pi);
    }
}
